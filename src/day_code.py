"""
Python code to find day code of a week
"""

def day_code(day_name):
    if day_name == "sun":
        return "001"
    elif day_name == "mon":
        return "002"
    elif day_name == "tue":
        return "003"
    elif day_name == "wed":
        return "004"
    elif day_name == "thu":
        return "005"
    elif day_name == "fri":
        return "006"
    elif day_name == "sat":
        return "007"
    else:
        return "No input provided"
        





